% Исходные данные
f = 1800;          % Частота в МГц
hBS = 30;          % Высота антенны базовой станции в метрах [30:200] м
hUE = 1;           % Высота антенны пользователя в метрах [1:1] м
d = 1;             % Расстояние в км
Lclutter = 3;      % Поправка на городскую застройку в дБ

% Коэффициенты для модели COST 231 Hata
A = 46.3;          % Коэффициент A для частот 1500-2000 МГц
B = 33.9;          % Коэффициент B для частот 1500-2000 МГц

% Расчет поправки на высоту антенны пользователя
log_f = log10(f);
a_hUE = (1.1 * log_f - 0.7) * hUE - (1.56 * log_f - 0.8);

% Расчет потерь пути распространения
log_hBS = log10(hBS);
log_d = log10(d);
s = 44.9 - 6.55 * log_hBS;    % Коэффициент s в зависимости от высоты BS

% Полная формула потерь
PL = A + B * log_f - 13.82 * log_hBS - a_hUE + s * log_d + Lclutter;

% Вывод результата
fprintf('Потери на расстоянии %.2f км: %.2f дБ\n', d, PL);
